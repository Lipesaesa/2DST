public class Aluno {
    private String endereco;
    private int rm;
    private String queroMeMatar;

    
    public String getEndereco() {
        return endereco;
    }
    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }
    public int getRm() {
        return rm;
    }
    public void setRm(int rm) {
        this.rm = rm;
    }
    public String getQueroMeMatar() {
        return queroMeMatar;
    }
    public void setQueroMeMatar(String queroMeMatar) {
        this.queroMeMatar = queroMeMatar;
    }

    

  
}